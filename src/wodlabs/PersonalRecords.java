package wodlabs;

public class PersonalRecords {
	
	int MaxUnbrokenPullUp;
	int MaxClean;
	int overHeadSquat;
	
	
	

	public int getOverHeadSquat() {
		return overHeadSquat;
	}

	public void setOverHeadSquat(int overHeadSquat) {
		this.overHeadSquat = overHeadSquat;
	}

	public int getMaxClean() {
		return MaxClean;
	}

	public void setMaxClean(int maxClean) {
		MaxClean = maxClean;
	}

	public int getMaxUnbrokenPullUp() {
		return MaxUnbrokenPullUp;
	}

	public void setMaxUnbrokenPullUp(int maxUnbrokenPullUp) {
		MaxUnbrokenPullUp = maxUnbrokenPullUp;
	}
	
	

}
